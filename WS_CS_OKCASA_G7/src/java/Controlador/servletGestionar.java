    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        EstadoDAO dao = new EstadoDAO();
        try {
            int id_h = Integer.parseInt(request.getParameter("txtId"));
            String estado = request.getParameter("txtEstado");
            if(dao.cambiarEstado(id_h,estado )){
                request.setAttribute("msj", "Estado Modificado con exito");
                request.getRequestDispatcher("gestion.jsp").forward(request, response);
            }
            else {
                request.setAttribute("err", "Estado NO Modificado");
                request.getRequestDispatcher("gestion.jsp").forward(request, response);
            }
        } catch (Exception e) {
            request.setAttribute("err", "Estado NO Modificado exception" + e.getMessage());
            request.getRequestDispatcher("gestion.jsp").forward(request, response);
        }
    }
